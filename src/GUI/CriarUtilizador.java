    private void bCriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCriarActionPerformed
        // TODO add your handling code here:
        
        
        String tipoUtilizador = ((String)this.jcTipoUtilizador.getSelectedItem());
        
        //Equipa
        if(tipoUtilizador.equals("Equipa")){
       
        Equipa equipa = new Equipa();
        
        equipa.setNome(txtNome.getText());
        
        int nCC = Integer.valueOf(txtNCC.getText());
        equipa.setnCC(nCC);
        
        
        int nif = Integer.valueOf(txtNif.getText());
        equipa.setNif(nif);
        
        int telef = Integer.valueOf(txtTelefone.getText());
        equipa.setTelefone(telef);
        
        equipa.setMorada(txtMorada.getText());
        
        equipa.setLocalidade(txtLocalidade.getText());
        
        equipa.setUsername(txtUsername.getText());
        
        equipa.setPassword(txtPassword.getText());
        
            try {
                Ficheiro.getRepo().addEquipa(equipa);
            } catch (Exception ex) {
                Logger.getLogger(CriarUtilizador.class.getName()).log(Level.SEVERE, null, ex);
                
            }
        Ficheiro.serializar("Repositorio.dat");
        JOptionPane.showMessageDialog(null, "Registado com sucesso!");
        }
        new AreaAdministrador().setVisible(true);
        dispose();
        
        
        //Gestor
        if(tipoUtilizador.equals("Gestor")){
       
        Gestor gestor = new Gestor();
        
        gestor.setNome(txtNome.getText());
        
        int nCC = Integer.valueOf(txtNCC.getText());
        gestor.setnCC(nCC);
        
        
        int nif = Integer.valueOf(txtNif.getText());
        gestor.setNif(nif);
        
        int telef = Integer.valueOf(txtTelefone.getText());
        gestor.setTelefone(telef);
        
        gestor.setMorada(txtMorada.getText());
        
        gestor.setLocalidade(txtLocalidade.getText());
        
        gestor.setUsername(txtUsername.getText());
        
        gestor.setPassword(txtPassword.getText());
        
            try {
                Ficheiro.getRepo().addGestor(gestor);
            } catch (Exception ex) {
                Logger.getLogger(CriarUtilizador.class.getName()).log(Level.SEVERE, null, ex);
                
            }
        Ficheiro.serializar("Repositorio.dat");
        JOptionPane.showMessageDialog(null, "Registado com sucesso!");
        }
        new AreaAdministrador().setVisible(true);
        dispose();
        
        
        //Municipio
        if(tipoUtilizador.equals("Municipio")){
       
        Municipio municipio = new Municipio();
        
        municipio.setNome(txtNome.getText());
        
        int nCC = Integer.valueOf(txtNCC.getText());
        municipio.setnCC(nCC);
        
        
        int nif = Integer.valueOf(txtNif.getText());
        municipio.setNif(nif);
        
        int telef = Integer.valueOf(txtTelefone.getText());
        municipio.setTelefone(telef);
        
        municipio.setMorada(txtMorada.getText());
        
        municipio.setLocalidade(txtLocalidade.getText());
        
        municipio.setUsername(txtUsername.getText());
        
        municipio.setPassword(txtPassword.getText());
        
            try {
                Ficheiro.getRepo().addMunicipio(municipio);
            } catch (Exception ex) {
                Logger.getLogger(CriarUtilizador.class.getName()).log(Level.SEVERE, null, ex);
                
            }
        Ficheiro.serializar("Repositorio.dat");
        JOptionPane.showMessageDialog(null, "Registado com sucesso!");
        }
        new AreaAdministrador().setVisible(true);
        dispose();
        
    }//GEN-LAST:event_bCriarActionPerformed

