private  void addInfoTable(){
	DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
	Object coluna[] = new Object[6];
	for(Utilizador u : Ficheiro.getRepo().getUtilizadores()){
		coluna[0] = u.getNome();
		coluna[1] = u.getnCC();
                coluna[2]= u.getNif();
		coluna[3] = u.getTelefone();
		coluna[4] = u.getMorada();
                coluna[5] = u.getLocalidade();
                coluna[6] = u.getUsername();

		modelo.addRow(coluna);
	}
} 

